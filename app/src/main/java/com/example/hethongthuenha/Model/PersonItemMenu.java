package com.example.hethongthuenha.Model;

public class PersonItemMenu {
    int imageItem;
    String name;

    public PersonItemMenu(int imageItem, String name) {
        this.imageItem = imageItem;
        this.name = name;
    }

    public int getImageItem() {
        return imageItem;
    }

    public String getName() {
        return name;
    }
}
